package operator;

public class LessGreaterthan {

	public static void main(String[] args) {
		int a =10;
		int b = 20;
		System.out.println(a>b);
		System.out.println(a<b);
		int a1 = 20;
		int b1 = 30;
		System.out.println(a1<b1);
		System.out.println(a1<=b1);
		
		System.out.println(a1>b1);
		System.out.println(a1>=b1);
		
		System.out.println(a1==b1);
		System.out.println(a1!=b1);
	}

}
