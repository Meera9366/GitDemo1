package javaLoops;

public class ForLoop7 {

	public static void main(String[] args) {
		// WAP to print numbers from 99 to 11
		// starting point = 99
		// Ending point = 11
		for(int i = 99; i>= 44; i--)
		{
			System.out.println(i);
		}
		//WAP to print 100 to 10 numbers in one line
		for(int j = 100; j>=10;j--)
		{
			System.out.print(j);
		}
			
	}

}
