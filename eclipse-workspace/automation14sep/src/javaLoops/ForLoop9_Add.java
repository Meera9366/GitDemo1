package javaLoops;

public class ForLoop9_Add {

	public static void main(String[] args) {
		//WAP for addition numbers form 5 to 10
		//Starting point = 5
		//Ending point = 10
		int addition =0;
		for(int i = 5; i <= 10; i++)
		{
			addition= addition +i;
		}
		System.out.println("Addition of 5 to 10 Numbers is------------>>  " +addition);
	}

}
