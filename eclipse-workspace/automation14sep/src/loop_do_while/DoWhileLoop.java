package loop_do_while;

public class DoWhileLoop {
	// WAP to print 1 to 5 number
	// Starting Number = 1;
	// Ending Number =5;
	// Increment by 1
	public static void main(String[] args) {
		int i =1;
		do
		{
			System.out.println(i);
			i++;
		}
		while(i<=5);
		{
			System.out.println("Program Completed");
		}

	}

}
